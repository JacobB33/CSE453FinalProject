    def __init__(self, mc: ModelConfig):
        super().__init__()
        self.mc = mc
        self.vocab_size = mc.vocab_size

        # Embeds token to dim size ie if dim is 2, 1 -> [x,y] (I think)
        self.tok_embeddings = tiktoken.encoding_for_model("gpt-4")

        decoder_layer = nn.TransformerDecoderLayer(mc.embedding_size, mc.nheads, )
        self.transformer = nn.TransformerDecoder()

        self.norm = RMSNorm(params.dim, eps=params.norm_eps)
        self.output = nn.Linear(
            params.dim, params.vocab_size, bias=False
        )

        self.freqs_cis = precompute_freqs_cis(
            self.params.dim // self.params.n_heads, self.params.max_seq_len * 2
        )

    # @torch.inference_mode()
    def forward(self, tokens: torch.Tensor, start_pos: int):
        _bsz, seqlen = tokens.shape
        h = self.tok_embeddings(tokens)
        self.freqs_cis = self.freqs_cis.to(h.device)
        freqs_cis = self.freqs_cis[start_pos: start_pos + seqlen]

        mask = None
        if seqlen > 1:
            mask = torch.full((1, 1, seqlen, seqlen), float("-inf"), device=tokens.device)
            mask = torch.triu(mask, diagonal=start_pos + 1).type_as(h)

        for layer in self.layers:
            h = layer(h, start_pos, freqs_cis, mask)
        h = self.norm(h)
        # output = self.output(h[:, -1, :])  # only compute last logits
        return self.output(h)
